TileMap::TileMap()
{
	space = TextureManager::loadTexture("assets/space.png");
	rock = TextureManager::loadTexture("assets/rock.png");
	other = TextureManager::loadTexture("assets/other.png");

	loadMap(tempLevel);

	src.x = src.y = 0;
	dest.x = dest.y = 0;

	src.w = src.h = 32;
	dest.w = dest.h = 32;
}

void TileMap::loadMap(int arr[20][25])
{
	for (int row = 0; row < 20; row++) {
		for (int col = 0; col < 25; col++) {
			map[row][col] = arr[row][col];
		}
	}
}

void TileMap::drawMap()
{
	int type = 0;
	for (int row = 0; row < 20; row++) {
		for (int col = 0; col < 25; col++) {
			type = map[row][col];
			
			dest.x = col * 32;
			dest.y = row * 32;

			switch (type)
			{
			case 0 :
				TextureManager::draw(space, src, dest);
				break;
			case 1:
				TextureManager::draw(rock, src, dest);
				break;
			case 2:
				TextureManager::draw(other, src, dest);
				break;
			default:
				break;
			}

		}
	}
}
