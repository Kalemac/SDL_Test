TileMap::TileMap()
{
	space = TextureManager::loadTexture_BMP("assets/space.bmp");
	rock = TextureManager::loadTexture_BMP("assets/rock.bmp");
	other = TextureManager::loadTexture_BMP("assets/other.bmp");

	loadMap(tempLevel);

	src.x = src.y = 0;
	dest.x = dest.y = 0;

	src.w = src.h = 32;
	dest.w = dest.h = 32;
}

void TileMap::loadMap(int arr[20][30])
{
	for (int row = 0; row < 20; row++) {
		for (int col = 0; col < 30; col++) {
			map[row][col] = arr[row][col];
		}
	}
}

void TileMap::drawMap()
{
	int type = 0;
	for (int row = 0; row < 20; row++) {
		for (int col = 0; col < 30; col++) {
			type = map[row][col];
			
			dest.x = col * 32;
			dest.y = row * 32;

			switch (type)
			{
			case 0 :
				TextureManager::draw(space, src, dest);
				break;
			case 1:
				TextureManager::draw(rock, src, dest);
				break;
			case 2:
				TextureManager::draw(other, src, dest);
				break;
			default:
				break;
			}

		}
	}
}
